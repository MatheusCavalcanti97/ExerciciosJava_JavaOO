package lista_04;

public class Pessoa {
	String nomePessoa;

	public Pessoa() {
		super();
	}

	public Pessoa(String nomePessoa) {
		super();
		this.nomePessoa = nomePessoa;
	}

	public String getNomePessoa() {
		return nomePessoa;
	}

	public void setNomePessoa(String nomePessoa) {
		this.nomePessoa = nomePessoa;
	}

}
